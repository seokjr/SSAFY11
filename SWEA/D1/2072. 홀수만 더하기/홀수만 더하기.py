T = int(input())
# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
for test_case in range(1, T + 1):
    # ///////////////////////////////////////////////////////////////////////////////////
    lst = list(map(int, input().split()))
    answer = 0
    for m in lst:
        if m % 2 == 1:
            answer += m
    print(f"#{test_case} {answer}")
    # ///////////////////////////////////////////////////////////////////////////////////
